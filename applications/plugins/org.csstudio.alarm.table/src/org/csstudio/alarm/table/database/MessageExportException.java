package org.csstudio.alarm.table.database;

public class MessageExportException extends Exception {

    public MessageExportException() {
        super();
    }

    public MessageExportException(String message, Throwable cause) {
        super(message, cause);
    }

    public MessageExportException(String message) {
        super(message);
    }

    public MessageExportException(Throwable cause) {
        super(cause);
    }

}
