package org.csstudio.nams.application.department.decision.exceptions;

public class InitPropertiesException extends Exception {

	private static final long serialVersionUID = -3633307291962751821L;

	public InitPropertiesException() {
	}

	public InitPropertiesException(String message) {
		super(message);
	}

	public InitPropertiesException(Throwable cause) {
		super(cause);
	}

	public InitPropertiesException(String message, Throwable cause) {
		super(message, cause);
	}

}
